local function Lower(value)
  if value == nil then
    return ""
  end
  return string.lower(tostring(value))
end

local function Trim(value)
  return (string.gsub(tostring(value or ""), "^%s*(.-)%s*$", "%1"))
end

local function ShortName(name)
  local short = Trim(tostring(name or ""))
  if short == "" then
    return nil
  end
  local dash = string.find(short, "-", 1, true)
  if dash then
    short = string.sub(short, 1, dash - 1)
  end
  short = Trim(short)
  return short ~= "" and short or nil
end

local function SamePlayerName(a, b)
  local left = ShortName(a)
  local right = ShortName(b)
  return left ~= nil and right ~= nil and Lower(left) == Lower(right)
end

local function EncodeField(text)
  text = tostring(text or "")
  return string.gsub(text, "([^%w _%.%-])", function(ch)
    return string.format("%%%02X", string.byte(ch))
  end)
end

local function DecodeField(text)
  text = tostring(text or "")
  return string.gsub(text, "%%(%x%x)", function(hex)
    local num = tonumber(hex, 16)
    if not num then return " " end
    return string.char(num)
  end)
end

local function SplitByLiteralSep(text, sep)
  local out = {}
  if text == nil then return out end
  text = tostring(text)
  if text == "" then
    table.insert(out, "")
    return out
  end
  local startPos = 1
  while startPos <= string.len(text) do
    local pos = string.find(text, sep, startPos, true)
    if pos then
      table.insert(out, string.sub(text, startPos, pos - 1))
      startPos = pos + string.len(sep)
    else
      table.insert(out, string.sub(text, startPos))
      break
    end
  end
  return out
end

local function BuildReserveKey(bossKey, itemId)
  return Lower(tostring(bossKey or "")) .. ":" .. tostring(tonumber(itemId) or 0)
end

local function CopyReserveEntries(rows)
  local copy = {}
  if type(rows) ~= "table" then
    return copy
  end
  for i = 1, table.getn(rows) do
    local reserve = rows[i]
    if type(reserve) == "table" then
      table.insert(copy, {
        bossKey = tostring(reserve.bossKey or ""),
        bossName = tostring(reserve.bossName or ""),
        itemId = tonumber(reserve.itemId) or 0,
        itemName = tostring(reserve.itemName or ""),
        icon = tostring(reserve.icon or ""),
        quality = tonumber(reserve.quality) or 0,
        slot = tostring(reserve.slot or ""),
      })
    end
  end
  return copy
end

