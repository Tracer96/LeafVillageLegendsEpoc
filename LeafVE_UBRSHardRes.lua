      end
    end
  end

  if existingIndex then
    LeafVE_RaidCatalogSource[existingIndex] = desired
  else
    table.insert(LeafVE_RaidCatalogSource, insertIndex, desired)
  end

  LeafVE.raidCatalog = nil
  LeafVE.raidCatalogOrder = nil
end

