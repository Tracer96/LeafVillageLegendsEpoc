local OriginalOnAddonMessage = LeafVE.OnAddonMessage
function LeafVE:OnAddonMessage(prefix, message, channel, sender)
  if prefix == "LeafVE" then
    if string.sub(message or "", 1, string.len(HARD_RES_PREFIX)) == HARD_RES_PREFIX then
      self:HandleIncomingRaidHardReserveMessage(string.sub(message, string.len(HARD_RES_PREFIX) + 1))
      return
    end
    if string.sub(message or "", 1, string.len(HARD_RES_SYNC_PREFIX)) == HARD_RES_SYNC_PREFIX then
      self:HandleIncomingRaidHardReserveMessage(string.sub(message, string.len(HARD_RES_SYNC_PREFIX) + 1))
      return
    end
  end
  return OriginalOnAddonMessage(self, prefix, message, channel, sender)
end

