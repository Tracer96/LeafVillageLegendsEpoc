function LeafVE:BroadcastRaidHardReserveSnapshotForEvent(eventRecord, messageType)
  if type(eventRecord) ~= "table" then
    return
  end
  for i = 1, table.getn(eventRecord.hardReserves or {}) do
    local reserve = eventRecord.hardReserves[i]
    if type(reserve) == "table" then
      self:BroadcastRaidHardReserve(eventRecord, reserve, messageType or HARD_RES_PREFIX)
    end
  end
end

function LeafVE:BroadcastRaidHardReserveSnapshot(force)
  if not InGuild() then return end
  local now = Now()
  self.lastRaidHardReserveRespondAt = self.lastRaidHardReserveRespondAt or 0
  if not force and (now - self.lastRaidHardReserveRespondAt) < 10 then
    return
  end
  self.lastRaidHardReserveRespondAt = now

  for _, eventRecord in pairs(self:GetRaidEventsDB()) do
    if type(eventRecord) == "table" then
      local updatedAt = tonumber(eventRecord.updatedAt or eventRecord.postedAt or 0) or 0
      if NormalizeRaidEventStatus(eventRecord.status) ~= "archived" and updatedAt >= (now - (14 * 86400)) then
        self:BroadcastRaidHardReserveSnapshotForEvent(eventRecord, HARD_RES_SYNC_PREFIX)
      end
    end
  end
end

function LeafVE:StoreRaidHardReserveEntry(eventId, reserveRecord)
  eventId = Trim(tostring(eventId or ""))
  if eventId == "" or type(reserveRecord) ~= "table" then
    return nil, false
  end

  local eventRecord = self:GetRaidEventsDB()[eventId]
  if type(eventRecord) ~= "table" then
    self.pendingRaidHardReserveSync = self.pendingRaidHardReserveSync or {}
    self.pendingRaidHardReserveSync[eventId] = self.pendingRaidHardReserveSync[eventId] or {}
    table.insert(self.pendingRaidHardReserveSync[eventId], reserveRecord)
    return nil, true
  end

  local priorSignature = self:BuildRaidHardReserveSignature(eventRecord.hardReserves or {})
  local merged = CopyReserveEntries(eventRecord.hardReserves)
  table.insert(merged, reserveRecord)
  eventRecord.hardReserves = self:NormalizeRaidHardReserveEntries(eventRecord, merged)
  local changed = priorSignature ~= self:BuildRaidHardReserveSignature(eventRecord.hardReserves)
  if changed and LeafVE.UI and LeafVE.UI.panels and LeafVE.UI.panels.raidSignups and LeafVE.UI.panels.raidSignups:IsVisible() and LeafVE.UI.RefreshRaidSignupPanel then
    LeafVE.UI:RefreshRaidSignupPanel(true)
  end
  return eventRecord, changed
end

function LeafVE:HandleIncomingRaidHardReserveMessage(payload)
  local fields = SplitByLiteralSep(payload or "", SEP)
  local eventId = DecodeField(fields[1] or "")
  local bossKey = DecodeField(fields[2] or "")
  local itemId = tonumber(fields[3]) or 0
  if eventId == "" or bossKey == "" or itemId <= 0 then
    return nil, false
  end
  return self:StoreRaidHardReserveEntry(eventId, { bossKey = bossKey, itemId = itemId })
end

