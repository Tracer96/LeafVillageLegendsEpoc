    panel.raidConsumablesBG:ClearAllPoints()
    panel.raidConsumablesBG:SetPoint("TOPLEFT", panel.raidConsumablesLabel, "BOTTOMLEFT", 0, -4)
  end

  if panel.createEventBtn then
    panel.createEventBtn.ownerPanel = panel
    panel.createEventBtn:SetScript("OnClick", function()
      local owner = this.ownerPanel
      local raidKey = owner.createRaidKey
      if not raidKey then
        owner.detailFeedbackText:SetText("|cFFFF6666No raid is selected.|r")
        return
      end
      local startAt, startErr = LeafVE_RaidUIParseTimestamp(owner.dateInput:GetText() or "", owner.startTimeInput:GetText() or "")
      if not startAt then
        owner.detailFeedbackText:SetText("|cFFFF6666" .. tostring(startErr or "Invalid start time.") .. "|r")
        return
      end
      local closeAt, closeErr = LeafVE_RaidUIParseTimestamp(owner.dateInput:GetText() or "", owner.closeTimeInput:GetText() or "")
      if not closeAt then
        owner.detailFeedbackText:SetText("|cFFFF6666" .. tostring(closeErr or "Invalid close time.") .. "|r")
        return
      end
      if closeAt > startAt then closeAt = startAt end
      local defaults = LeafVE:GetRaidDefaultRoleTargets(raidKey, nil)
      local catalogEntry = LeafVE:GetRaidCatalogEntry(raidKey)
      local raidSize = tonumber(catalogEntry and catalogEntry.raidSize) or 20
      local hardReserves = LeafVE:IsRaidHardReserveOnly(raidKey) and LeafVE_RaidUICollectHardReserveEntries(owner.selectedHardReserveEntries) or {}
      local stored, err = LeafVE:CreateRaidEvent(
        raidKey,
        owner.selectedRaidNameText:GetText() or "",
        startAt,
        closeAt,
        raidSize,
        defaults,
        owner.raidNotesInput and (owner.raidNotesInput:GetText() or "") or "",
        owner.raidConsumablesInput and (owner.raidConsumablesInput:GetText() or "") or "",
        LeafVE:IsRaidHardReserveOnly(raidKey) and 0 or (owner.createSoftReserveCount or 1),
        hardReserves
      )
      if not stored then
        owner.detailFeedbackText:SetText("|cFFFF6666" .. tostring(err or "Unable to create raid event.") .. "|r")
        return
      end
      if owner.raidNotesInput then owner.raidNotesInput:SetText("") end
      if owner.raidConsumablesInput then owner.raidConsumablesInput:SetText("") end
      owner.selectedHardReserveEntries = {}
      owner.mode = "upcoming"
      owner.selectedEventId = stored.id
      owner.lastDetailEventId = nil
      owner.detailFeedbackText:SetText("|cFF88FF88Raid event posted and synced.|r")
      LeafVE.UI:RefreshRaidSignupPanel(true)
    end)
  end

  if panel.softReservePopup and panel.softReservePopup.clearBtn then
    panel.softReservePopup.clearBtn.ownerPanel = panel
    panel.softReservePopup.clearBtn:SetScript("OnClick", function()
      local owner = this.ownerPanel
      local popup = owner.softReservePopup
      if popup and popup.mode == "hard" then
        owner.selectedHardReserveEntries = {}
        LeafVE_RaidUIRefreshSoftReservePopup(owner)
        return
      end
      local reserveCount = LeafVE:GetEffectiveRaidSoftReserveCount(owner.selectedEvent)
      local slotIndex = math.min(math.max(1, tonumber(popup.activeSlot) or 1), reserveCount)
      owner.selectedSoftReserveSlots = owner.selectedSoftReserveSlots or {}
      owner.selectedSoftReserveSlots[slotIndex] = nil
      LeafVE_RaidUIRefreshSoftReservePopup(owner)
    end)
  end

  if panel.softReservePopup and panel.softReservePopup.lootRowButtons then
    for i = 1, table.getn(panel.softReservePopup.lootRowButtons) do
      local row = panel.softReservePopup.lootRowButtons[i]
      if row then
        row.ownerPanel = panel
        row:SetScript("OnClick", function()
          local owner = this.ownerPanel
          local popup = owner.softReservePopup
          if not popup or not this.lootInfo then return end
          local chosenBossKey = tostring((this.lootInfo and this.lootInfo.bossKey) or popup.selectedBossKey or "")
          if chosenBossKey ~= "" then
            popup.selectedBossKey = chosenBossKey
          end

          if popup.mode == "hard" then
            owner.selectedHardReserveEntries = owner.selectedHardReserveEntries or {}
            local matchKey = BuildReserveKey(chosenBossKey, this.lootInfo.itemId)
            local removed = false
            for index = table.getn(owner.selectedHardReserveEntries), 1, -1 do
              local reserve = owner.selectedHardReserveEntries[index]
              if type(reserve) == "table" and BuildReserveKey(reserve.bossKey, reserve.itemId) == matchKey then
                table.remove(owner.selectedHardReserveEntries, index)
                removed = true
              end
            end
            if not removed then
              table.insert(owner.selectedHardReserveEntries, {
                bossKey = chosenBossKey,
                bossName = LeafVE:GetRaidBossName(owner.createRaidKey, chosenBossKey),
                itemId = tonumber(this.lootInfo.itemId) or 0,
                itemName = tostring(this.lootInfo.name or ""),
                icon = tostring(this.lootInfo.icon or ""),
                quality = tonumber(this.lootInfo.quality) or 0,
                slot = tostring(this.lootInfo.slot or ""),
              })
            end
            owner.selectedHardReserveEntries = LeafVE:NormalizeRaidHardReserveEntries({ raidKey = owner.createRaidKey }, owner.selectedHardReserveEntries)
            LeafVE_RaidUIRefreshSoftReservePopup(owner)
            return
          end

          local eventRecord = owner.selectedEvent
          local reserveCount = LeafVE:GetEffectiveRaidSoftReserveCount(eventRecord)
          local slotIndex = math.min(math.max(1, tonumber(popup.activeSlot) or 1), reserveCount)
          owner.selectedSoftReserveSlots = owner.selectedSoftReserveSlots or {}
          owner.selectedSoftReserveSlots[slotIndex] = {
            bossKey = chosenBossKey,
            bossName = LeafVE:GetRaidBossName(eventRecord and eventRecord.raidKey, chosenBossKey),
            itemId = tonumber(this.lootInfo.itemId) or 0,
            itemName = tostring(this.lootInfo.name or ""),
            icon = tostring(this.lootInfo.icon or ""),
            quality = tonumber(this.lootInfo.quality) or 0,
            slot = tostring(this.lootInfo.slot or ""),
          }
          for reserveIndex = 1, reserveCount do
            if reserveIndex ~= slotIndex then
              local other = owner.selectedSoftReserveSlots[reserveIndex]
              if type(other) == "table" and tonumber(other.itemId) == tonumber(this.lootInfo.itemId) and tostring(other.bossKey or "") == chosenBossKey then
                owner.selectedSoftReserveSlots[reserveIndex] = nil
              end
            end
          end
          if reserveCount == 2 and slotIndex == 1 and owner.selectedSoftReserveSlots[2] == nil then
            popup.activeSlot = 2
          end
          LeafVE_RaidUIRefreshSoftReservePopup(owner)
        end)
        row:SetScript("OnLeave", function()
          if this.isChosen then
            this:SetBackdropColor(0.12, 0.04, 0.04, 0.98)
            this:SetBackdropBorderColor(1.00, 0.40, 0.40, 1.00)
          else
            this:SetBackdropColor(0.05, 0.05, 0.07, 0.95)
            this:SetBackdropBorderColor(0.30, 0.30, 0.35, 1.00)
          end
          GameTooltip:Hide()
        end)
      end
    end
  end
end

local OriginalRefreshRaidSignupPanel = LeafVE.UI.RefreshRaidSignupPanel
function LeafVE.UI:RefreshRaidSignupPanel(skipRequest)
  OriginalRefreshRaidSignupPanel(self, skipRequest)

  local panel = self.panels and self.panels.raidSignups
  if not panel then return end
  EnsureRaidSignupPanelHardReserveControls(panel)

  if panel.mode == "admin" then
    local canCreate = LeafVE:IsRaidEventCreatorRank()
    local isHardOnly = LeafVE:IsRaidHardReserveOnly(panel.createRaidKey)
    if isHardOnly then
      panel.createSoftReserveCount = 0
      panel.selectedHardReserveEntries = LeafVE:NormalizeRaidHardReserveEntries({ raidKey = panel.createRaidKey }, panel.selectedHardReserveEntries or {})
    end

    if panel.hardReserveLabel then if isHardOnly then panel.hardReserveLabel:Show() else panel.hardReserveLabel:Hide() end end
    if panel.hardReserveBtn then
      if isHardOnly then panel.hardReserveBtn:Show() else panel.hardReserveBtn:Hide() end
      LeafVE_RaidUISetButtonEnabled(panel.hardReserveBtn, canCreate and isHardOnly)
    end
    if panel.hardReserveText then
      if isHardOnly then
        panel.hardReserveText:SetText(BuildHardReservePreviewText(panel.selectedHardReserveEntries or {}, true))
        panel.hardReserveText:Show()
      else
        panel.hardReserveText:Hide()
      end
    end

    if panel.softReserveCountText and isHardOnly then
      panel.softReserveCountText:SetText("|cFFAAAAAAUpper Blackrock Spire uses |cFFFF6666Hard Res|r|cFFAAAAAA only. Soft reserves stay off for this raid.|r")
    end
    LeafVE_RaidUISetButtonEnabled(panel.softReserveCountOneBtn, canCreate and not isHardOnly)
    LeafVE_RaidUISetButtonEnabled(panel.softReserveCountTwoBtn, canCreate and not isHardOnly)

    if isHardOnly then
      panel.detailMeta:SetText(canCreate and "|cFF88FF88Anbu, Sannin, and Hokage can post guild raid events here.|r\n|cFFFF6666Upper Blackrock Spire uses hard reserves only.|r" or "|cFFFF6666Only Anbu, Sannin, and Hokage can post raid events. Everyone else can still sign up.|r")
      local previewEvent = { raidKey = panel.createRaidKey, hardReserves = panel.selectedHardReserveEntries or {} }
      panel.detailBody:SetText((panel.createRaidKey and LeafVE_RaidUIGetBossSectionText(panel.createRaidKey) or "|cFF888888No raid catalog available.|r") .. "\n\n|cFFFF6666Hard Res|r\n" .. LeafVE_RaidUIGetHardReserveBodyText(previewEvent))
    end
    return
  end

  if panel.hardReserveLabel then panel.hardReserveLabel:Hide() end
  if panel.hardReserveBtn then panel.hardReserveBtn:Hide() end
  if panel.hardReserveText then panel.hardReserveText:Hide() end

  local selectedEvent = panel.selectedEvent
  if type(selectedEvent) == "table" and LeafVE:IsRaidHardReserveOnly(selectedEvent) then
    local eventStatus = NormalizeRaidEventStatus(selectedEvent.status)
    local isClosed = eventStatus == "completed" or eventStatus == "cancelled" or eventStatus == "archived"
    local statusText = LeafVE_RaidUIGetStatusMarkup(selectedEvent.status)
    local countdownText = LeafVE_RaidUIFormatCountdown(selectedEvent.startAt)
    local countdownMarkup = LeafVE_RaidUIColorText(countdownText, 0.85, 0.85, 0.88)
    if string.find(countdownText, "Started ", 1, true) then
      countdownMarkup = LeafVE_RaidUIColorText(countdownText, 1.00, 0.72, 0.28)
    end
    panel.detailMeta:SetText("|cFF88CCFF" .. tostring(selectedEvent.raidName or "Raid") .. "|r  " .. statusText .. "\n|cFFFFD700Lead:|r " .. tostring(selectedEvent.postedBy or "") .. "  |cFFFFD700Start:|r " .. LeafVE_RaidUIFormatTime(selectedEvent.startAt) .. "  |cFFFFD700Close:|r " .. LeafVE_RaidUIFormatTime(selectedEvent.signupCloseAt) .. "\n|cFFFF6666Hard Res:|r " .. tostring(table.getn(selectedEvent.hardReserves or {})) .. " item(s)  " .. countdownMarkup)
    panel.detailBody:SetText(
      "|cFFFFD700Need|r\n" .. LeafVE_RaidUIGetNeedMarkup(selectedEvent) ..
      "\n\n|cFF88CCFFNotes|r\n" .. ((selectedEvent.notes and selectedEvent.notes ~= "") and tostring(selectedEvent.notes) or "|cFF888888None|r") ..
      "\n\n|cFFFFD700Required Consumables|r\n" .. ((selectedEvent.consumables and selectedEvent.consumables ~= "") and tostring(selectedEvent.consumables) or "|cFF888888None listed.|r") ..
      "\n\n|cFFFF6666Hard Res|r\n" .. LeafVE_RaidUIGetHardReserveBodyText(selectedEvent) ..
      "\n\n|cFFFFD700Bosses|r\n" .. LeafVE_RaidUIGetBossSectionText(selectedEvent.raidKey)
    )
    panel.softReserveBtn:SetText("HR Only")
    LeafVE_RaidUISetButtonEnabled(panel.softReserveBtn, false)
    LeafVE_RaidUISetButtonEnabled(panel.withdrawBtn, not isClosed)
    LeafVE_RaidUISetButtonEnabled(panel.saveSignupBtn, not isClosed)
    if panel.softReservePopup and panel.softReservePopup:IsShown() and panel.softReservePopup.mode ~= "hard" then
      LeafVE_RaidUICloseSoftReservePopup(panel)
    end
  end
end
