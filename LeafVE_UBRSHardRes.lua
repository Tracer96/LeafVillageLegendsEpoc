local OriginalStoreRaidEventRecord = LeafVE.StoreRaidEventRecord
function LeafVE:StoreRaidEventRecord(record)
  local eventId = type(record) == "table" and Trim(tostring(record.id or "")) or ""
  local existing = eventId ~= "" and self:GetRaidEventsDB()[eventId] or nil
  local preservedHardReserves = CopyReserveEntries(type(existing) == "table" and existing.hardReserves or nil)
  local stored, changed = OriginalStoreRaidEventRecord(self, record)
  if type(stored) ~= "table" then
    return stored, changed
  end

  local merged = {}
  if type(record) == "table" and record.hardReserves ~= nil then
    merged = CopyReserveEntries(record.hardReserves)
  else
    merged = preservedHardReserves
  end

  if self.pendingRaidHardReserveSync and type(self.pendingRaidHardReserveSync[stored.id]) == "table" then
    local pending = self.pendingRaidHardReserveSync[stored.id]
    for i = 1, table.getn(pending) do
      table.insert(merged, pending[i])
    end
    self.pendingRaidHardReserveSync[stored.id] = nil
  end

  local priorSignature = self:BuildRaidHardReserveSignature(stored.hardReserves or {})
  stored.hardReserves = self:NormalizeRaidHardReserveEntries(stored, merged)
  local hardResChanged = priorSignature ~= self:BuildRaidHardReserveSignature(stored.hardReserves)
  if hardResChanged and LeafVE.UI and LeafVE.UI.panels and LeafVE.UI.panels.raidSignups and LeafVE.UI.panels.raidSignups:IsVisible() and LeafVE.UI.RefreshRaidSignupPanel then
    LeafVE.UI:RefreshRaidSignupPanel(true)
  end
  return stored, changed or hardResChanged
end

local OriginalCreateRaidEvent = LeafVE.CreateRaidEvent
function LeafVE:CreateRaidEvent(raidKey, title, startAt, signupCloseAt, raidSize, roleTargets, notes, consumables, softReserveCount, hardReserves)
  if self:IsRaidHardReserveOnly(raidKey) then
    softReserveCount = 0
  end
  local stored, err = OriginalCreateRaidEvent(self, raidKey, title, startAt, signupCloseAt, raidSize, roleTargets, notes, consumables, softReserveCount)
  if not stored then
    return stored, err
  end
  stored.hardReserves = self:NormalizeRaidHardReserveEntries(stored, hardReserves or {})
  local eventRecord = self:GetRaidEventsDB()[tostring(stored.id or "")]
  if type(eventRecord) == "table" then
    eventRecord.hardReserves = CopyReserveEntries(stored.hardReserves)
  end
  if table.getn(stored.hardReserves or {}) > 0 then
    self:BroadcastRaidHardReserveSnapshotForEvent(stored, HARD_RES_PREFIX)
  end
  if LeafVE.UI and LeafVE.UI.panels and LeafVE.UI.panels.raidSignups and LeafVE.UI.panels.raidSignups:IsVisible() and LeafVE.UI.RefreshRaidSignupPanel then
    LeafVE.UI:RefreshRaidSignupPanel(true)
  end
  return stored
end

