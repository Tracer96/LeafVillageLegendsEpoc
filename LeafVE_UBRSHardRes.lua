function LeafVE:NormalizeRaidHardReserveEntries(eventRecord, reserves)
  local rows = {}
  if type(reserves) ~= "table" then
    return rows
  end

  local allowedBosses = {}
  if type(eventRecord) == "table" then
    for _, boss in ipairs(self:GetRaidSoftReserveBosses(eventRecord)) do
      allowedBosses[tostring(boss.key)] = tostring(boss.name or boss.key or "Boss")
    end
  end

  local seen = {}
  for i = 1, table.getn(reserves) do
    local reserve = reserves[i]
    if type(reserve) == "table" then
      local bossKey = Trim(tostring(reserve.bossKey or ""))
      local itemId = tonumber(reserve.itemId)
      if bossKey ~= "" and itemId and itemId > 0 and (next(allowedBosses) == nil or allowedBosses[bossKey]) then
        local dedupeKey = BuildReserveKey(bossKey, itemId)
        if not seen[dedupeKey] then
          local lootRow = self:FindRaidSoftReserveLootItem(bossKey, itemId)
          local itemName = Trim(tostring((lootRow and lootRow.name) or reserve.itemName or ""))
          if itemName ~= "" then
            table.insert(rows, {
              bossKey = bossKey,
              bossName = string.sub(tostring(allowedBosses[bossKey] or reserve.bossName or self:GetRaidBossName(eventRecord and eventRecord.raidKey, bossKey) or bossKey), 1, 48),
              itemId = itemId,
              itemName = string.sub(itemName, 1, 64),
              icon = tostring((lootRow and lootRow.icon) or reserve.icon or "INV_Misc_QuestionMark"),
              quality = tonumber((lootRow and lootRow.quality) or reserve.quality) or 0,
              slot = string.sub(tostring((lootRow and lootRow.slot) or reserve.slot or ""), 1, 80),
            })
            seen[dedupeKey] = true
          end
        end
      end
    end
  end

  table.sort(rows, function(a, b)
    local aBoss = Lower(a.bossName or a.bossKey or "")
    local bBoss = Lower(b.bossName or b.bossKey or "")
    if aBoss == bBoss then
      return Lower(a.itemName or "") < Lower(b.itemName or "")
    end
    return aBoss < bBoss
  end)

  return rows
end

function LeafVE:BuildRaidHardReserveSignature(reserves)
  local parts = {}
  if type(reserves) ~= "table" then
    return ""
  end
  for i = 1, table.getn(reserves) do
    local reserve = reserves[i]
    if type(reserve) == "table" then
      table.insert(parts, BuildReserveKey(reserve.bossKey, reserve.itemId))
    end
  end
  table.sort(parts)
  return table.concat(parts, "|")
end

